export async function getServerSideProps() {
  const prisma = new PrismaClient();
  const response = await prisma.resource.findMany({      include: {
    resourceTags: true,
  },
});
  // Pass the data to the Home page
  return {
    props: {
      resources: JSON.parse(JSON.stringify(response)),
    },
  };
}